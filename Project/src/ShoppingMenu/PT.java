    public static void sumPrice(){
        sum_price = 0;
        for(int i = 0; i< jTable1.getRowCount();i++){
            sum_price = sum_price + Double.parseDouble(jTable1.getValueAt(i, 1).toString());
        }
//        lb_sumprice.setText(String.valueOf(sum_price));
        lb_sumprice.setText(String.valueOf(sum_price));
    }

